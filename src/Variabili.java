public class Variabili {
    public static void main(String[] args) {
        // Dichiarazione di una variabile intera
        int numero;  // Variabile dichiarata ma non inizializzata

        // Inizializzazione della variabile
        numero = 10;

        // Oppure, dichiarazione e inizializzazione insieme
        int altroNumero = 20;

        // Dichiarazione e inizializzazione di una stringa (testo)
        String messaggio = "Ciao, mondo!";

        // Stampa delle variabili
        System.out.println("Numero: " + numero);
        System.out.println("Altro Numero: " + altroNumero);
        System.out.println("Messaggio: " + messaggio);

        // Tipo primitivo: intero
        int eta = 25;

        // Tipo primitivo: decimale
        double pi = 3.14159;

        // Tipo primitivo: carattere singolo
        char iniziale = 'A';

        // Tipo primitivo: booleano (vero o falso)
        boolean isJavaFun = true;

        // Tipo di riferimento: stringa
        String saluto = "Ciao!";

        // Stampa delle variabili
        System.out.println("Età: " + eta);
        System.out.println("Pi greco: " + pi);
        System.out.println("Iniziale: " + iniziale);
        System.out.println("Java è divertente? " + isJavaFun);
        System.out.println("Saluto: " + saluto);
    }
}

