/*
 * The HelloWorld class is a simple Java program that serves as
 * a basic example of how to print output to the console.
 *
 * Main Features:
 *
 *     - The main method is the entry point of the program. It is
 *       where the execution starts.
 *     - The System.out.println() method is used to print the
 *       string "Hello World" to the console.
 */
public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World");
    }
}

